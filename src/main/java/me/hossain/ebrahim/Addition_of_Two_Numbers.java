package me.hossain.ebrahim;

// This is a simple Java program to add two numbers
public class Addition_of_Two_Numbers {

    public static void main(String[] args) {

        // Declare and initialize two integer variables with values
        int num1 = 5, num2 = 10, sum;

        // Perform the addition of num1 and num2 and store the result in 'sum'
        sum = num1 + num2;

        // Print the result of the addition
        System.out.println("Sum of " + num1 + " and " + num2 + " = " + sum);
    }
}

